import { Schema } from "mongoose";
import { BookModel, IBook } from "../interface/book.interface";
import { model } from "mongoose";

const bookSchema = new Schema<IBook>(
  {
    title: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    genre: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Books = model<IBook, BookModel>("User", bookSchema);
export default Books;
